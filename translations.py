def t(lang, key, **kwargs):
    lang = lang if lang in TRANSLATIONS else 'en'
    text = TRANSLATIONS[lang].get(key, TRANSLATIONS['en'].get(key, key))
    if kwargs:
        try:
            return text.format(**kwargs)
        except (KeyError, IndexError):
            return text
    return text
